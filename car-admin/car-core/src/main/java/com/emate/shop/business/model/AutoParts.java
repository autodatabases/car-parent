package com.emate.shop.business.model;

import java.math.BigDecimal;
import java.util.Date;

public class AutoParts {
    /**
     * 
     * @Table auto_parts.id
     */
    private Long id;

    /**
     * 配件类型
     * @Table auto_parts.parts_type
     */
    private String partsType;

    /**
     * 
     * @Table auto_parts.parts_type_id
     */
    private Long partsTypeId;

    /**
     * 品牌
     * @Table auto_parts.brand
     */
    private String brand;

    /**
     * 产品型号
     * @Table auto_parts.product_code
     */
    private String productCode;

    /**
     * 产品详细名称
     * @Table auto_parts.detail_name
     */
    private String detailName;

    /**
     * 结算价格
     * @Table auto_parts.inner_price
     */
    private BigDecimal innerPrice;

    /**
     * 销售价格
     * @Table auto_parts.sale_price
     */
    private BigDecimal salePrice;

    /**
     * 产品图片
     * @Table auto_parts.pic
     */
    private String pic;

    /**
     * 产品尺寸
     * @Table auto_parts.size
     */
    private String size;

    /**
     * 产品属性
     * @Table auto_parts.properties
     */
    private String properties;

    /**
     * 
     * @Table auto_parts.create_time
     */
    private Date createTime;

    /**
     * 
     * @Table auto_parts.update_time
     */
    private Date updateTime;

    /**
    * 获取 
    * @return id
    */
    public Long getId() {
        return id;
    }

    /**
    * 设置 
    * @param id
    */
    public void setId(Long id) {
        this.id = id;
    }

    /**
    * 获取 配件类型
    * @return partsType
    */
    public String getPartsType() {
        return partsType;
    }

    /**
    * 设置 配件类型
    * @param partsType
    */
    public void setPartsType(String partsType) {
        this.partsType = partsType;
    }

    /**
    * 获取 
    * @return partsTypeId
    */
    public Long getPartsTypeId() {
        return partsTypeId;
    }

    /**
    * 设置 
    * @param partsTypeId
    */
    public void setPartsTypeId(Long partsTypeId) {
        this.partsTypeId = partsTypeId;
    }

    /**
    * 获取 品牌
    * @return brand
    */
    public String getBrand() {
        return brand;
    }

    /**
    * 设置 品牌
    * @param brand
    */
    public void setBrand(String brand) {
        this.brand = brand;
    }

    /**
    * 获取 产品型号
    * @return productCode
    */
    public String getProductCode() {
        return productCode;
    }

    /**
    * 设置 产品型号
    * @param productCode
    */
    public void setProductCode(String productCode) {
        this.productCode = productCode;
    }

    /**
    * 获取 产品详细名称
    * @return detailName
    */
    public String getDetailName() {
        return detailName;
    }

    /**
    * 设置 产品详细名称
    * @param detailName
    */
    public void setDetailName(String detailName) {
        this.detailName = detailName;
    }

    /**
    * 获取 结算价格
    * @return innerPrice
    */
    public BigDecimal getInnerPrice() {
        return innerPrice;
    }

    /**
    * 设置 结算价格
    * @param innerPrice
    */
    public void setInnerPrice(BigDecimal innerPrice) {
        this.innerPrice = innerPrice;
    }

    /**
    * 获取 销售价格
    * @return salePrice
    */
    public BigDecimal getSalePrice() {
        return salePrice;
    }

    /**
    * 设置 销售价格
    * @param salePrice
    */
    public void setSalePrice(BigDecimal salePrice) {
        this.salePrice = salePrice;
    }

    /**
    * 获取 产品图片
    * @return pic
    */
    public String getPic() {
        return pic;
    }

    /**
    * 设置 产品图片
    * @param pic
    */
    public void setPic(String pic) {
        this.pic = pic;
    }

    /**
    * 获取 产品尺寸
    * @return size
    */
    public String getSize() {
        return size;
    }

    /**
    * 设置 产品尺寸
    * @param size
    */
    public void setSize(String size) {
        this.size = size;
    }

    /**
    * 获取 产品属性
    * @return properties
    */
    public String getProperties() {
        return properties;
    }

    /**
    * 设置 产品属性
    * @param properties
    */
    public void setProperties(String properties) {
        this.properties = properties;
    }

    /**
    * 获取 
    * @return createTime
    */
    public Date getCreateTime() {
        return createTime;
    }

    /**
    * 设置 
    * @param createTime
    */
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    /**
    * 获取 
    * @return updateTime
    */
    public Date getUpdateTime() {
        return updateTime;
    }

    /**
    * 设置 
    * @param updateTime
    */
    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}