package com.emate.wechat.menu;


/**
 * 普通按钮（子按钮） 
 * CommonButton.java
 * @author likk
 * 2015-7-22 下午03:07:40
 *
 */
public class CommonButton extends Button {  
    private String type;  
    private String key;  
  
    public String getType() {  
        return type;  
    }  
  
    public void setType(String type) {  
        this.type = type;  
    }  
  
    public String getKey() {  
        return key;  
    }  
  
    public void setKey(String key) {  
        this.key = key;  
    }  
}  