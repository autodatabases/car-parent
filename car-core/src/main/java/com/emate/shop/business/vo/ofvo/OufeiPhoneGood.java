package com.emate.shop.business.vo.ofvo;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "cardinfo")
public class OufeiPhoneGood {
	private String err_msg;//描述
	private String retcode;//错误码
	private String cardid;//商品编号
	private String cardname;//商品描述
	private String inprice;//商品价格
	private String game_area;//商品所属地区
	public String getErr_msg() {
		return err_msg;
	}
	public void setErr_msg(String err_msg) {
		this.err_msg = err_msg;
	}
	public String getRetcode() {
		return retcode;
	}
	public void setRetcode(String retcode) {
		this.retcode = retcode;
	}
	public String getCardid() {
		return cardid;
	}
	public void setCardid(String cardid) {
		this.cardid = cardid;
	}
	public String getCardname() {
		return cardname;
	}
	public void setCardname(String cardname) {
		this.cardname = cardname;
	}
	public String getInprice() {
		return inprice;
	}
	public void setInprice(String inprice) {
		this.inprice = inprice;
	}
	public String getGame_area() {
		return game_area;
	}
	public void setGame_area(String game_area) {
		this.game_area = game_area;
	}
	@Override
	public String toString() {
		return "OufeiPhoneGood [err_msg=" + err_msg + ", retcode=" + retcode + ", cardid=" + cardid + ", cardname="
				+ cardname + ", inprice=" + inprice + ", game_area=" + game_area + "]";
	}
	
}
