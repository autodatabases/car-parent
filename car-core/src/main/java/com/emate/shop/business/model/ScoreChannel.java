package com.emate.shop.business.model;

import java.math.BigDecimal;
import java.util.Date;

public class ScoreChannel {
    /**
     * id
     * @Table score_channel.id
     */
    private Long id;

    /**
     * 城市名称
     * @Table score_channel.city_name
     */
    private String cityName;

    /**
     * 渠道
     * @Table score_channel.channel
     */
    private String channel;

    /**
     * 服务包 ruleType
     */
    public static final java.lang.Integer RULE_TYPE_0 = 0;
    /**
     * 小保养 ruleType
     */
    public static final java.lang.Integer RULE_TYPE_1 = 1;
    /**
     * 规则类型：0，服务包，1，小保养
     * @Table score_channel.rule_type
     */
    private Integer ruleType;

    /**
     * 保单金额最小值
     * @Table score_channel.minimum
     */
    private BigDecimal minimum;

    /**
     * 保单金额最大值
     * @Table score_channel.maximum
     */
    private BigDecimal maximum;

    /**
     * 积分规则的结果:小保养,喷漆,洗车,电瓶，20元滴滴券，50元滴滴券
     * @Table score_channel.rule_value
     */
    private String ruleValue;

    /**
     * 小保养车型
     * @Table score_channel.car_type
     */
    private String carType;

    /**
     * 1元等于多少积分
     * @Table score_channel.score
     */
    private BigDecimal score;

    /**
     * 创建时间
     * @Table score_channel.create_time
     */
    private Date createTime;

    /**
     * 更新时间
     * @Table score_channel.update_time
     */
    private Date updateTime;

    /**
    * 获取 id
    * @return id
    */
    public Long getId() {
        return id;
    }

    /**
    * 设置 id
    * @param id
    */
    public void setId(Long id) {
        this.id = id;
    }

    /**
    * 获取 城市名称
    * @return cityName
    */
    public String getCityName() {
        return cityName;
    }

    /**
    * 设置 城市名称
    * @param cityName
    */
    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    /**
    * 获取 渠道
    * @return channel
    */
    public String getChannel() {
        return channel;
    }

    /**
    * 设置 渠道
    * @param channel
    */
    public void setChannel(String channel) {
        this.channel = channel;
    }

    /**
    * 获取 规则类型：0，服务包，1，小保养
    * @return ruleType
    */
    public Integer getRuleType() {
        return ruleType;
    }

    /**
    * 设置 规则类型：0，服务包，1，小保养
    * @param ruleType
    */
    public void setRuleType(Integer ruleType) {
        this.ruleType = ruleType;
    }

    /**
    * 获取 保单金额最小值
    * @return minimum
    */
    public BigDecimal getMinimum() {
        return minimum;
    }

    /**
    * 设置 保单金额最小值
    * @param minimum
    */
    public void setMinimum(BigDecimal minimum) {
        this.minimum = minimum;
    }

    /**
    * 获取 保单金额最大值
    * @return maximum
    */
    public BigDecimal getMaximum() {
        return maximum;
    }

    /**
    * 设置 保单金额最大值
    * @param maximum
    */
    public void setMaximum(BigDecimal maximum) {
        this.maximum = maximum;
    }

    /**
    * 获取 积分规则的结果:小保养,喷漆,洗车,电瓶，20元滴滴券，50元滴滴券
    * @return ruleValue
    */
    public String getRuleValue() {
        return ruleValue;
    }

    /**
    * 设置 积分规则的结果:小保养,喷漆,洗车,电瓶，20元滴滴券，50元滴滴券
    * @param ruleValue
    */
    public void setRuleValue(String ruleValue) {
        this.ruleValue = ruleValue;
    }

    /**
    * 获取 小保养车型
    * @return carType
    */
    public String getCarType() {
        return carType;
    }

    /**
    * 设置 小保养车型
    * @param carType
    */
    public void setCarType(String carType) {
        this.carType = carType;
    }

    /**
    * 获取 1元等于多少积分
    * @return score
    */
    public BigDecimal getScore() {
        return score;
    }

    /**
    * 设置 1元等于多少积分
    * @param score
    */
    public void setScore(BigDecimal score) {
        this.score = score;
    }

    /**
    * 获取 创建时间
    * @return createTime
    */
    public Date getCreateTime() {
        return createTime;
    }

    /**
    * 设置 创建时间
    * @param createTime
    */
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    /**
    * 获取 更新时间
    * @return updateTime
    */
    public Date getUpdateTime() {
        return updateTime;
    }

    /**
    * 设置 更新时间
    * @param updateTime
    */
    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}