package com.emate.shop.business.vo.gyvo;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * @file OilCardGameInfo.java
 * @author xieguijin
 * @mail guijin.xie@emateglobal.com
 * @since 2017年6月9日
 */
@XmlRootElement(name = "gameinfo")
public class OilCardGameInfo {

    private String gameid;
    private String gamename;
    public OilCardGameInfo(String gameid, String gamename) {
        super();
        this.gameid = gameid;
        this.gamename = gamename;
    }
    public OilCardGameInfo() {
        super();
    }
    
    public String getGameid() {
        return gameid;
    }
    
    public void setGameid(String gameid) {
        this.gameid = gameid;
    }
    
    public String getGamename() {
        return gamename;
    }
    
    public void setGamename(String gamename) {
        this.gamename = gamename;
    }
    @Override
    public String toString() {
        return "OilCardGameInfo [gameid=" + gameid + ", gamename=" + gamename
                + "]";
    }
    
}
